package ch03.declarativeVSimperative;

public class IntegerWithMy0wnAlgorithmStatic {
    private int a;
    private int b;
    private int c;

    public IntegerWithMy0wnAlgorithmStatic(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getC() {
        return c;
    }
}
